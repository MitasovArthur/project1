package Prectik_2;

public class Comparison {
    public static void main( String [] args){
        byte value1 = 0;
        byte value2 = 1;
        boolean result;   // значения возврощаеться в булевом видео тру или фолз
        result = value1 < value2;
        System.out.println(result);

        result = value1 > value2;
        System.out.println(result);

        result = value1 >= value2;
        System.out.println(result);

        result = value1 <= value2;
        System.out.println(result);

        result = value1 == value2;
        System.out.println(result);

        result = value1 != value2;
        System.out.println(result);



    }
    }
